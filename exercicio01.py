
# teste para ver se o número é maior, menor ou igual a 10

numero = int(input('Digite um numero:'))

if (numero>10):
    print('O número é maior que 10')  
       
elif(numero==10):
    print('O número é 10')    
       
else:
    print('O número é menor que 10')
    
    
    
    
    



    
    
    