# Verificar qual foi o tipo de voto

# 1 = Branco
# 2 = verde
# 3 = vermelho

print('Programa para saber o seu voto')

numero = int(input('Escolha uma opção 1(Branco), 2(Verde)ou 3(Vermelho):'))

if (numero==1):
    print('Branco, Obrigada pelo seu voto!')  
       
elif(numero==2):
    print('verde, Obrigada pelo seu voto!') 
       
elif(numero==3):
    print('vermelho, Obrigada pelo seu voto!')   
                   
else:
    print('Voto errado, tente novamente.')

    
    
    
